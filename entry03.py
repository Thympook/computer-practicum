b = int(input('b: '))
c = int(input('c: '))

print(f'–ù–û–î —Ç—Ä—ë—Ö –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–µ–ª: {nod(nod(a, b), c)}')
